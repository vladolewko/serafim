    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const container = document.getElementById('carousel-container');
            const prevBtn = document.getElementById('prev-btn');
            const nextBtn = document.getElementById('next-btn');
            const indicators = document.querySelectorAll('.indicator');

            let currentSlide = 0;
            const totalItems = 13;
            const itemsVisible = 5;
            const maxSlide = totalItems - itemsVisible; // 8 позицій (0-8)

            function updateCarousel() {
                // Рухаємо контейнер на 20% (100% / 5 елементів = 20%)
                const translateX = currentSlide * 20;
                container.style.transform = `translateX(-${translateX}%)`;

                // Оновлюємо індикатори
                indicators.forEach((indicator, index) => {
                    if (index === currentSlide) {
                        indicator.classList.remove('bg-blue-400');
                        indicator.classList.add('bg-yellow-400', 'active');
                    } else {
                        indicator.classList.remove('bg-yellow-400', 'active');
                        indicator.classList.add('bg-blue-400');
                    }
                });
            }

            function nextSlide() {
                if (currentSlide < maxSlide) {
                    currentSlide++;
                    updateCarousel();
                }
            }

            function prevSlide() {
                if (currentSlide > 0) {
                    currentSlide--;
                    updateCarousel();
                }
            }

            // Обробники подій
            nextBtn.addEventListener('click', nextSlide);
            prevBtn.addEventListener('click', prevSlide);

            // Обробники для індикаторів
            indicators.forEach((indicator, index) => {
                indicator.addEventListener('click', () => {
                    currentSlide = index;
                    updateCarousel();
                });
            });

            // Ініціалізація
            updateCarousel();

            // Автоматичне прокручування (опціонально)
